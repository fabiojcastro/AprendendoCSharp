using System;

namespace _10_PraticandoFor
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Projeto 10 - Praticando o uso do For.");

            for (int cont = 1; cont <= 10; cont++)
            {
                Console.WriteLine("A contagem é " + cont);
            }

            Console.ReadLine();
        }
    }
}
